CREATE TRIGGER `after_events_insert` AFTER INSERT ON `events` FOR EACH ROW BEGIN
	IF NEW.event_id = 101 THEN
		SET @penalty_id = '';
		SET @penalty_type = '';
		SET @penalize = FALSE;
		SET @event_time = STR_TO_DATE(CONCAT(NEW.`date`, ' ', NEW.`time`), '%d/%m/%Y %H:%i:%s');
		SELECT option_value INTO @minutes_before_entry FROM btmms.ed_options WHERE option_name = 'minutes_before_entry' LIMIT 1;
		SELECT option_value INTO @minutes_after_exit FROM btmms.ed_options WHERE option_name = 'minutes_after_exit' LIMIT 1;
		
        SET @ar = TRUE;
		IF NEW.boom_id = 1 THEN
			SET @st = 'B';
            SET @ar = FALSE;
			SELECT STR_TO_DATE(CONCAT(DATE(reserved_time), ' ', `time`, ':00'), '%Y-%m-%d %H:%i:%s') INTO @reserved_time FROM btmms.ed_reservations WHERE STATUS = 'A' ORDER BY reserved_time LIMIT 1;
			SET @expected_time = DATE_ADD(@reserved_time, INTERVAL (-1 * @minutes_before_entry) MINUTE);
			IF @event_time > @expected_time THEN
				SET @penalty_id = 2;
				SET @penalty_type = 'Late Entry for Departure';
				SET @penalize = TRUE;
			END IF;
		END IF;
		
		IF NEW.boom_id = 4 THEN
			SET @st = 'D';
            SET @ar = FALSE;
			SELECT reserved_time INTO @reserved_time FROM btmms.ed_reservations WHERE STATUS IN ('A', 'DL') ORDER BY reserved_time LIMIT 1;
			SET @expected_time = DATE_ADD(@reserved_time, INTERVAL @minutes_after_exit MINUTE);
			IF @event_time > @expected_time THEN
				SET @penalty_id = 3;
				SET @penalty_type = 'Late Exit for Departure';
				SET @penalize = TRUE;
			END IF;
		END IF;
        
        IF NEW.boom_id = 3 THEN
			SET @st = 'B';
            SET @ar = TRUE;
			SELECT reserved_time INTO @reserved_time FROM btmms.ed_reservations WHERE STATUS IN ('A', 'p', 'DL') ORDER BY reserved_time LIMIT 1;
			SET @expected_time = DATE_ADD(@reserved_time, INTERVAL @minutes_after_exit MINUTE);
			IF @event_time > @expected_time THEN
				SET @penalty_id = 4;
				SET @penalty_type = 'Late Entry for Arrival';
				SET @penalize = TRUE;
			END IF;
		END IF;
        
        IF NEW.boom_id = 2 THEN
			SET @st = 'D';
            SET @ar = TRUE;
			SELECT reserved_time INTO @reserved_time FROM btmms.ed_reservations WHERE STATUS IN ('A', 'p', 'DL') ORDER BY reserved_time LIMIT 1;
			SET @expected_time = DATE_ADD(@reserved_time, INTERVAL @minutes_after_exit MINUTE);
			IF @event_time > @expected_time THEN
				SET @penalty_id = 5;
				SET @penalty_type = 'Late Exit for Arrival';
				SET @penalize = TRUE;
			END IF;
		END IF;
		
        IF @ar = TRUE THEN
			UPDATE btmms.ed_ar_reservations SET `status` = @st, `reservation_status` = @st WHERE (`status` IN ('A', 'p', 'B', 'DL') OR `reservation_status` IN ('A', 'B', 'DL')) AND bus_id = NEW.card_no AND DATE(reserved_time) = DATE(NOW());
        ELSE
			UPDATE btmms.ed_reservations SET `status` = @st, `reservation_status` = @st WHERE (`status` IN ('A', 'p', 'B', 'DL') OR `reservation_status` IN ('A', 'B', 'DL')) AND bus_id = NEW.card_no AND DATE(reserved_time) = DATE(NOW());
		END IF;
		INSERT INTO btmms.ed_eventHolder (cardNO, gateNO, _time, event_id) VALUES (NEW.card_no, NEW.boom_id, NEW.time, NEW.event_id);
		
		IF @penalize = TRUE THEN
			SELECT operator_id INTO @operator_id FROM btmms.probase_tbl_bus WHERE id = NEW.card_no LIMIT 1;
			INSERT INTO btmms.ed_penalties (created_at, updated_at, bus_operator_id, bus_id, date_booked, date_paid, `status`, `type`, penalty_type_id) VALUES (NOW(), NOW(), @operator_id, NEW.card_no, NOW(), '0000-00-00 00:00:00', 'Unpaid', @penalty_type, @penalty_id);
		END IF;
	END IF;
